import { ApiProperty } from "@nestjs/swagger";

export class RecordTransactionValidationErrorDto {
  @ApiProperty({
    description: 'Código do status HTTP',
    example: 400
  })
  statusCode: number;

  @ApiProperty({
    description: 'Mensagem principal do erro',
    example: 'Validation failed'
  })
  message: string;

  @ApiProperty({
    description: 'Lista de erros específicos de validação',
    example: [
      'assetName: Nome do ativo deve ter pelo menos 3 caracteres',
      'quantity: Quantidade deve ser um número positivo',
      'price: Preço deve ser um número positivo',
      'dateAt: Data da transação não pode ser no futuro'
    ],
    type: [String]
  })
  details: string[];

  @ApiProperty({
    description: 'Timestamp do erro',
    example: '2024-01-15T10:30:00Z'
  })
  timestamp: string;

  @ApiProperty({
    description: 'Caminho da requisição',
    example: '/uuid-123-456-789/transactions/buy'
  })
  path: string;
}

